package clase;

public class rectangulo {
	private int base;
	private int altura;
	
		
	public rectangulo(int base ,int altura ) {
		this.altura= altura;
		this.base = base ;
		
		
	}
	
	/**
	 * calcula el area de l rectangulo
	 *@return el area del rectangulo 
	 */
	
	public int arrea(){
		
	return base * altura ;
			
	}
	
	
	}
